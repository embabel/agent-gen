/**
 * GuardRail For User Messages
 */
class UserTestInputGuardRail : UserInputGuardRail {

    private val logger = LoggerFactory.getLogger(javaClass)
    override val name: String
        get() = "UserTestInputGuardRail"

    override val description: String
        get() = "UserTestInputGuardRail"

    public override fun validate(input: String, blackboard: Blackboard): ValidationResult {
        logger.info("Validated Simple User Input {}", input)
        blackboard["masking"] = "masking"
        return ValidationResult(true, mutableListOf<ValidationError>())
    }
}

class TestingTransformer(blackBoard: Blackboard?) : ToolLoopTransformer {
    val logger = LoggerFactory.getLogger(TestingTransformer::class.java)
    val bb = blackBoard
    override fun transformBeforeLlmCall(context: BeforeLlmCallContext): List<Message> {
        val masking = bb?.get("masking")
        logger.info("Get Masking from Blackboard  Before LLM Call {}", masking)
        return context.history
    }

    override fun transformAfterLlmCall(context: AfterLlmCallContext): Message {
        val masking = AgentProcess.get()?.blackboard?.get("masking")
        logger.info("Get Masking from Blackboard After LLm Call {}", masking)
        return context.history[0]
    }
}

/**
 * TestAgent - Generated by MetaAgent
 *
 * Test agent for unit testing
 */
@Agent(description = "Test agent for unit testing")
class TestAgent {

    private val logger = LoggerFactory.getLogger(javaClass)

    @AchievesGoal(description = "perform_test_completed")
    @Action(
        description = "perform test",
        cost = 0.5,
        value = 0.8,
    )
    @ToolGroup("default")
    fun performtest(input: UserInput, context: OperationContext): String {
        logger.info("performtest called with input: \${input.content}")

        val comparison = context.promptRunner()
            .withGuardRails(UserTestInputGuardRail())
            .withToolLoopTransformers(TestingTransformer(AgentProcess.get()?.blackboard))
            .generateText(
                """
               You are Testing Agent

               Just Perform Unit testing.
               
            """.trimIndent()
            )
        val masking = AgentProcess.get()?.blackboard?.get("masking")
        logger.info("Get Final Masking from Blackboard {}", masking)
        return "Completed: perform test"
    }
}